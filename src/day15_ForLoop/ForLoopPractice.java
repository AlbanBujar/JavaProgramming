package day15_ForLoop;

public class ForLoopPractice {
    public static void main(String[] args) {
        //15 16 17 18 ......44

        /*.......*/
        for (int i=15; i<=44; i++ ){
            System.out.print(i+" ");
        }
        System.out.println();
        System.out.println("Hello World");

        System.out.println("-------------------------");

        // 100 99 98 97 ........50

        for (int i=100; i>=50; i-- ){
            System.out.println(i);
        }


    }
}